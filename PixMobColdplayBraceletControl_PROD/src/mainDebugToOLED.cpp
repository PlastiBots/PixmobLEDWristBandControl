/* PixMobIRForwarder, 

   Modifed by Dave A. July 16 2025:
   Extracts coded data sent via Serial from https://ivanr3d.com/tools/led-wristband/, and displays it on the OLED, then can be embedded below as new arrays for LED Sequences.
   Later will be used to have an ESP32 operate as a hand-held IR remote to trigger multiple PixMob LED wristbands

   Steps:
   1) Setup ESP32C3, OLED and IR LED. Download code
   2) Go to web page above and connect to ESP32
   3) Run an effect and fade as necessary.  Note - not all of these combinations work. Start with just an LED effect
   4) Once you find one you like, you can create new arrays and functions for that colour. You will have to manually enter the numbers in the aray
   5) Run the new LED sequence to enure it works.
   6) The intent of this is to build a series of sequence arrays that you can then integrate into another "IR blaster" project to control the Pixmob LED wristbands
    
   Hardware:
   ESP32C3: https://vi.aliexpress.com/item/1005005879592718.html?gatewayAdapt=glo2vnm
   SH1106 I2C OLED 128x64


   Source and credits to:
   https://github.com/danielweidman/pixmob-ir-reverse-engineering/blob/main/arduino_sender/PixMob_Transmitter_ESP32/PixMob_Transmitter_ESP32.ino
   https://github.com/crankyoldgit/IRremoteESP8266/wiki#ir-sending
   https://ivanr3d.com/tools/led-wristband/
   
   IR Definitions: https://github.com/danielweidman/pixmob-ir-reverse-engineering/blob/main/www/js/effects_definitions.js

   U8G2:  https://github.com/olikraus/u8g2/blob/master/sys/arduino/u8g2_full_buffer/HelloWorld/HelloWorld.ino


   Version 1.0, August 2022
   Daniel Weidman, danielweidman.com
   
   Based on Ken Shirriff's IrsendDemo
   Copyright 2009 Ken Shirriff, http://arcfn.com

   This script takes IR codes for PixMob bracelets from a connected computer over Serial or 
   other device over Bluetooth and transmits those codes to PixMob bracelets.

    ------------------------------------------------------------------------------
   An IR LED circuit *MUST* be connected to the Arduino on a pin
   as specified by kIrLed below.

   TL;DR: The IR LED needs to be driven by a transistor for a good result.

   Suggested circuit:
       https://github.com/crankyoldgit/IRremoteESP8266/wiki#ir-sending

   Common mistakes & tips:
 *   * Don't just connect the IR LED directly to the pin, it won't
       have enough current to drive the IR LED effectively.
 *   * Make sure you have the IR LED polarity correct.
       See: https://learn.sparkfun.com/tutorials/polarity/diode-and-led-polarity
 *   * Typical digital camera/phones can be used to see if the IR LED is flashed.
       Replace the IR LED with a normal LED if you don't have a digital camera
       when debugging.
 *   * Avoid using the following pins unless you really know what you are doing:
 *     * Pin 0/D3: Can interfere with the boot/program mode & support circuits.
 *     * Pin 1/TX/TXD0: Any serial transmissions from the ESP8266 will interfere.
 *     * Pin 3/RX/RXD0: Any serial transmissions to the ESP8266 will interfere.
*/

